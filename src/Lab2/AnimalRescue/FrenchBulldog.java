package Lab2.AnimalRescue;

public class FrenchBulldog extends Dog{
    @Override
    public void Name() {
        System.out.println("My dog's name is Masha");
    }

    @Override
    public void Age() {
        System.out.println("Masha is 4 years old");
    }

    @Override
    public void Color() {
        System.out.println("White with black spots");
    }

    @Override
    public void FavFood() {
        System.out.println("She eats avocado and salmon");
    }

    @Override
    public void eat() {
        System.out.println("my dog eats very loud");
    }

    @Override
    public void FavActivity() {
        System.out.println("Plays with kids");
    }

    @Override
    public void sleep() {
        System.out.println("ZZZZzzzzzz");
    }

    @Override
    public void speak() {
        System.out.println("Bark!");
    }

    @Override
    public void Weight() {
        System.out.println("12,5 kg");
    }

    @Override
    public void walk() {
        System.out.println("Run all day long");
    }

    @Override
    public void Height() {
        System.out.println("Height is 45 cm");
    }
}
